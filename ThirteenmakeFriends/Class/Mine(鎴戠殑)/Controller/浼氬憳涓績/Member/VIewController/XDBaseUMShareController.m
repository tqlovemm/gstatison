/**
 网页分享
 */
- (void)shareWebPageToPlatformType:(UMSocialPlatformType)platformType {
    //创建分享消息对象
    UMSocialMessageObject *messageObject = [UMSocialMessageObject messageObject];
    

    if (platformType == UMSocialPlatformType_Sina) {
        NSString *text = self.UMS_Web_Desc;
        if (self.UMS_Web_Desc.length>=100) {
           text = [[self.UMS_Web_Desc substringToIndex:100]stringByAppendingString:@"..."];
        }
        
        messageObject.text = [NSString stringWithFormat:@"%@%@",text,self.UMS_WebLink];
        UMShareImageObject *shareObject = [[UMShareImageObject alloc]init];
        shareObject.thumbImage = self.UMS_THUMB_IMAGE;
        [shareObject setShareImage:self.UMS_THUMB_IMAGE];
        messageObject.shareObject = shareObject;
    }else{
        //创建网页内容对象
        //    NSString* thumbURL =  self.UMS_THUMB_IMAGE;
        UMShareWebpageObject *shareObject = [UMShareWebpageObject shareObjectWithTitle:self.UMS_Title descr:self.UMS_Web_Desc thumImage:self.UMS_THUMB_IMAGE];
        //设置网页地址
        shareObject.webpageUrl = self.UMS_WebLink;
        //分享消息对象设置分享内容对象
        messageObject.shareObject = shareObject;
    }
   
  
    
    
    //调用分享接口
    [[UMSocialManager defaultManager] shareToPlatform:platformType messageObject:messageObject currentViewController:self completion:^(id data, NSError *error) {
        if (error) {
            UMSocialLogInfo(@"************Share fail with error %@*********",error);
        }else{
            if ([data isKindOfClass:[UMSocialShareResponse class]]) {
                UMSocialShareResponse *resp = data;
                //分享结果消息
                UMSocialLogInfo(@"response message is %@",resp.message);
                //第三方原始返回的数据
                UMSocialLogInfo(@"response originalResponse data is %@",resp.originalResponse);
                
            }else{
                UMSocialLogInfo(@"response data is %@",data);
            }
        }
        [self alertWithError:error];
    }];
}

